import Foundation


class p4 {
    
    static func parseTextIntoArrays(input: String) -> [[Character]] {
        var result: [[Character]] = []
        let lines = input.split(separator: "\n") 
         
        for line in lines {
            let chars = line
            result.append(Array(chars))
        }
        
        return result
    }
    
    static func problem1(){
        let input = parseTextIntoArrays(input: input4)
        let numberOfRow = input.count
        let numberOfColumn = input[0].count
        let word = "XMAS"
        let wordLength = word.count
        let directions = [
            //vertical, horizontal
            (0,1), //right
            (0,-1), //left
            (1,0), //down
            (-1,0), //up
            (1,1), //down-right
            (-1,1), //up-right
            (1,-1), //down-left
            (-1,-1), //up-left
        ]
        
        
        func checkDirection(startRow: Int, startCol: Int, dirRow: Int, dirCol: Int) -> Bool {
            
            for i in 0..<wordLength {

                let newRow = startRow + dirRow * i
                let newCol = startCol + dirCol * i
                

                if newRow >= numberOfRow || newCol >= numberOfColumn || newRow < 0 || newCol < 0 {
                    return false
                }
                if input[newRow][newCol] != Array(word)[i] {
                    return false
                }
            }
            return true
        }
        
        
       var count = 0
        for row in 0..<numberOfRow {
            for col in 0..<numberOfColumn {

                for (dirRow, dirCol) in directions {
                    if checkDirection(startRow: row, startCol: col, dirRow: dirRow, dirCol: dirCol) {
                        count += 1
                    }
                }
            }
        }
        
        
        print("number of \(word) found: \(count)")
    }
    
    static func problem1part2(){
        
        var input = parseTextIntoArrays(input: input4)
        
        var numberOfRow = input.count
        var numberOfColumn = input[0].count
        var word = "MAS"
  
        func isMASorSAM(m: Character, a: Character, s: Character) -> Bool{
            var formedWord = String([m, a, s])
            return formedWord == word || String(formedWord.reversed()) == word
        }
        
        
       var count = 0
        
        for row in 1..<(numberOfRow - 1) {
            for col in 1..<(numberOfColumn - 1) {
                let tlbr = isMASorSAM(
                    m: input[row - 1][col - 1],
                    a: input[row][col],
                    s: input[row + 1][col + 1])
                let bltr = isMASorSAM(
                    m: input[row + 1][col - 1],
                    a: input[row][col],
                    s: input[row - 1][col + 1])
               
                if tlbr && bltr {
                    count += 1
                }
            }
        }
        
        
        print("number of x-\(word) found: \(count)")
    }
    
}


